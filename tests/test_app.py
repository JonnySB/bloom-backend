def test_plant_by_user(
    db_connection, test_web_address
):
    db_connection.seed("seeds/bloom.sql")
    plant_data = {
        "common_name": "Plant Name", 
        "latin_name": "Latin Plant Name", 
        "photo": "photo title", 
        "watering_frequency": 1
        }
    response = requests.get(f"http://{test_web_address}/1", json=plant_data)
    assert response.status_code == 200
    assert response.json() == {
        "common_name": "Plant Name", 
        "latin_name": "Latin Plant Name", 
        "photo": "photo title", 
        "watering_frequency": 1
        }